def addition(num1, num2)
  math=num1 + num2
  return math
end

def subtraction(num1, num2)
  math=num1 - num2
  return math
end

def division(num1, num2)
  math=num1 / num2
  return math
end

def multiplication(num1, num2)
  math=num1 * num2
end

